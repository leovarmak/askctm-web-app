<h2 class="w3-center" ><strong> History Of College </strong></h2>
<p>
The Anakapalli Merchants Associations was started in the year 1942. It is one of the oldest Associations in Andhra Pradesh. The establishment of the Associations was primary to discharge social obligations.</p>

<p>The founding fathers have established this Association. A Maternity Hospital was established in 1945, A.M.A.L. College in 1953, English Medium High School in 1965, Elementary school was in 2004 and ASK College Of Technology and Management in the year 2010.</p>
